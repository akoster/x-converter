package xcon.hotel.db;


public class HotelNetworkException extends RuntimeException {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public HotelNetworkException() {
        super();
    }

    public HotelNetworkException(String message, Throwable cause) {
        super(message, cause);
    }

    public HotelNetworkException(String message) {
        super(message);
    }

    public HotelNetworkException(Throwable cause) {
        super(cause);
    }
    
    

}
