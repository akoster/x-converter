package xcon.hotel.db;


public class RmiConnectionException extends Exception  {

    private static final long serialVersionUID = 1L;

    public RmiConnectionException() {
        super();
    }

    public RmiConnectionException(String message, Throwable cause) {
        super(message, cause);
    }

    public RmiConnectionException(String message) {
        super(message);
    }

    public RmiConnectionException(Throwable cause) {
        super(cause);
    }
    


}
