package xcon.stackoverflow.xml;

public class Child {

	private String key;
	private String value;

	public Child() {
	}

	public Child(String key) {
		this.key = key;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
