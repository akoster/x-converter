package xcon.hotel.db;


public class SwingGuiException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public SwingGuiException() {
        super();
    }

    public SwingGuiException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public SwingGuiException(String arg0) {
        super(arg0);
    }

    public SwingGuiException(Throwable arg0) {
        super(arg0);
    }

}
